export default {
  name: "roomList",
  data(){
    return {
      rooms : [],
    }
  },
  created() {
    this.getRoomByForm();
  },
  methods : {
      getRoomByForm() {
        this.$request.post("https://localhost:7103/user/room/get-all-by" , this.formData).then((res) => {
            this.rooms = res.data;
        })
      },
      ...mapActions(['saveFormDate']),
      reservationRoom() {
        const startDate = this.formData.checkIn.value;
        const endDate = this.formData.checkOut.value;
        const numberOfDay = Math.ceil((endDate.getTime() - startDate.getTime()) / (1000 * 60 * 60 *24)
        );
        const formDate = {startDate , endDate , numberOfDay}
        this.$store.dispatch('saveFormDate' , formDate);
        // this.$router.push({name : 'Room.book'})
        console.log("xin chao booking Room");
      }
  },
  computed: {
    // ...mapGetters(['getFormData']), // Sử dụng getter để truy cập dữ liệu
    formData() {
      return this.$store.state.formData; // Hoặc truy cập trực tiếp từ state
    }
    
  },
  
}
</script>

